#include <stdio.h>

int main()
{
    int a, b;

    a = 10;
    b = 3;

    printf("The addition is: %d \n", a + b);
    printf("The subtraction is: %d \n", a - b);
    printf("The multiplication is: %d \n", a * b);
    printf("The division is: %d \n", a / b);
    printf("The modules is: %d \n", a % b);

    return 0;
}